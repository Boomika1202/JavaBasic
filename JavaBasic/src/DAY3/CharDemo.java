package DAY3;

public class CharDemo {
	public static void main(String[]args) {
		char ch = 'a';
		System.out.println(ch);
		
		char ch1 = 65;
		System.out.println(ch1);
		
		char varl = '\u00A7';
		System.out.println(varl);
		
		char var2 = '\u20AC';
		System.out.println(var2);
		
		int a = 'A';
		System.out.println(a);
		
		
	
	}

}
